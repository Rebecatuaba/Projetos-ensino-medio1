    private void buttokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttokActionPerformed
        // TODO add your handling code here
         numero = Double.parseDouble(Textonmero.getText());
        
        if(n==0){
            menor=numero;
            maior=numero;
        } 
       
        
        if(numero <= menor){
            menor = numero;
        } 
        if(numero>= maior){
            maior = numero;
        }
        
        media= media + numero;
        
        Textonmero.setText("");
        Textonmero.requestFocus();
        
        n++;
          
    }//GEN-LAST:event_buttokActionPerformed

